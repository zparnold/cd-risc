                                <h1 class="page_title">Frequently Asked Questions</h1>
                               <b>1.	Can the CD-RISC be used by all age groups?:</b>
<br><br>
The scale was originally developed in various cohorts of adults, but there have 
been many studies in which the scale was given to children and adolescents from 
ages 10 to 18. The results of these studies suggest that the scale performs 
satisfactorily in these age groups. There have also been several studies in 
which the scale served as a valid measure of resilience in older age groups, 
e.g. above age 65.
<br><br>

<b>2.	Are there translations of the CD-RISC?</b>
<br /><br />
Over 90 approved translations of the CD-RISC currently exist in the following languages:
<br />
Afrikaans, Albanian, Amharic, Arabic, Armenian, Assamese, Azeri, Bahasa Indonesia, Bahasa Malaysia, Bemba, Bangla (Bengali), Bosnian, Burmese, Cebuano, Chinese (Taiwan and Peoples Republic), Croatian, Creole, Czech, Danish, Dari, Dutch, Estonian, Farsi, Finnish, Flemish, French (France, Belgium, Canada), Flemish, Georgian, German, Greek, Hakha Chin (CD-RISC-2), Hebrew, Hindi, Hungarian, Icelandic, isiXhosa, isiZulu, Italian, Japanese, Kannada, Khasi, Kashmiri, Kinyarwanda, Kiswahili, Khmer (CD-RISC-10), Korean, Kurdish, Latvian (CD-RISC 10), Liberian, Lingala (CD-RISC-10), Lithuanian, Luganda (CD-RISC-10), Luo, Luxembourgish (CD-RISC-10), Macedonian, Malagasy, Malayalam, Maltese (RISC-10 only), Marathi, Mongolian, Nepali, Norwegian, Pashto (CD-RISC-2 and CD-RISC-10), Polish, Portuguese (Europe, Brazil), Punjabi, Quechua, Romanian, Russian, Serbian, Slovak (all versions), Slovenian, Somali (CD-RISC-2), Spanish (Europe, Caribbean, South America, Central America), Swedish, Tagalog, Tamil, Telugu, Thai, Tigrinya (CD-RISC-2), Tok Pisin/Papua New Guinea, Turkish, Ukrainian, Urdu, Vietnamese, Welsh, Xitsonga.
<br />
We are sometimes asked if translations have been validated. A standard procedure is followed in preparing the translations, which are reviewed jointly by translators and scale creators and it is assumed that the translation will be sufficiently close to the original so as to give the same performance, but not accounting for local or population effects. Therefore, more comprehensive validation and reliability testing has to be done later by those using the translation. The degree to which this is done varies from not at all to extensively.
<br><br>

<b>3.	 Are all translations of the scale authorized?</b>
<br /><br />
We have become aware of some unauthorized translations of the CD-RISC, i.e. 
they have been undertaken without permission of either Dr Connor or Dr Davidson. 
Some of these translations have been based on an incomplete version of the 
scale and cannot be considered valid or reliable.
<br /><br />
If you are interested in using a translation, we encourage you to contact us 
before proceeding. If you wish to undertake a translation, please contact us 
for details of the standard translation procedure.
<br /><br />
The scale can only be obtained directly from Dr. Connor or Dr. Davidson, 
after an agreement has been completed.
<br><br>

<b>4.	Do unauthorized forms of the CD-RISC exist?</b>
<br /><br />
The CD-RISC-2, CD-RISC-10 and CD-RISC-25 are the only versions authorized for use. Attention is drawn to the existence of unauthorized
versions, created without permission and/or in violation of copyright. These altered versions of the CD-RISC have sometimes undergone significant modifications either in item wording, item ordering, addition or removal of items, change in the scoring choices or blending with other items to create entirely new scales. They include a 25-item scale appearing in a textbook, a 27-item scale known as A Modified CD-RISC: Including Previously Unaccounted Resilience Variables, a 34-item Expanded Kinyarwanda version, 6 CD-RISC items subsumed in abbreviated form in a longer geriatric measure, a 29-item Arabic translation, a 3-item version of the scale, an 18-item Ghanaian scale, a CD-RISC-8 and a 17-item Spanish scale. In addition, the authors at one time published results using an 11-item scale, which we have discontinued and should not be used. Other versions in which the scale is described as "modified", "expanded", "improved" etc. should not be used or disseminated.
<br><br>
<b>
At least three unvalidated versions of the CD-RISC have appeared in the literature. 
These have neither been properly tested nor should be used. (1) An 11-item CD-RISC 
was briefly used by the developers of the scale and subsequently abandoned; 
(2) a 25 item version of the scale has appeared in a textbook, and has been 
altered from the original scale; (3) a 27-item CD-RISC has been presented as a 
modification and improvement on the original scale, with many changes having been 
made based on an incomplete version of the CD-RISC. (4) Fourthly, some authors have 
taken the scale directly from Connor and Davidson (2003) in which we described 
the essential features of the scale. This too is an incomplete presentation of 
the CD-RISC and should not be used.
</b>
<br /><br />
<b>
Any other versions of the CD-RISC that have appeared in the literature, and/or which have been described using terms such as "modified", "expanded", "improved", "CD-RISC-8", are unauthorized and should not be used or disseminated.
</b>
<br><br>  

<b>5.	 How long does it take to complete the CD-RISC?</b>
<br /><br />
The 25 item scale can be completed in about 5-10 minutes unless there are 
complicating factors. The 10 and 2 item scales can be completed in between 
1 and 5 minutes. If difficulty is encountered in completing the scale 
(e.g. reading problems, poor concentration, etc), it may be helpful to obtain 
assistance of a colleague or staff person who can explain items or read them out slowly. 
<br><br>

<b>6.	 What are the uses of a resilience scale such as the CD-RISC?</b>
<br /><br />
The scale has been developed and tested as (i) a measure of degree of 
resilience, (ii) as a predictor of outcome to treatment with medication or 
psychotherapy, stress management and resilience-building; (iii) as a marker of 
progress during treatment; (iv) as a marker of biological (i.e. physical) 
changes in the brain. The scale also has promise as a method to screen people 
for high, intermediate or low resilience. 
<br /><br />
Any of the three forms of the CD-RISC can be used in the ways described in the 
above paragraph. The decision as to which one to use is usually guided by the 
time available, and a judgement as to what kinds of information are to be 
obtained. For example, items relating to spirituality and faith are to be 
found only in the 25 item scale. For a very brief measure, such as an early 
screen or a lengthy questionnaire where "space" is at a premium, the 2 item 
scale may be most suitable. For a factorially homogeneous scale which focuses 
mainly on hardiness, the 10 item scale would be particularly suitable.
<br><br>

<b>7.	 I have completed a dissertation which used the CD-RISC to measure 
resilience. Can I include the scale on a university website as part of my dissertation?</b>
<br /><br />
The scale is copyright and not in the public domain, and unfortunately we are 
not able to grant permission for its reproduction in any form of print or 
electronic medium. We would be pleased to discuss individual situations as needed. 
<br><br>

<b>8.	 The scale is described in your publication in Depression and Anxiety 
(Connor & Davidson, 2003). Can I then use it directly from the publication?</b>
<br /><br />
The scale content and item sequence are presented but the item wording is 
incomplete and several important details are missing and the complete 
CD-RISC does not appear in this report. Therefore any representation of the 
scale which is derived from the Depression and Anxiety publication is 
incomplete, and the scale cannot be reconstructed from this publication. 
<br><br>

<b>9.	 How do I handle missing responses?</b>
<br /><br />
We recommend that missing responses are scored at the mean for the other items.  
In order to consider the scale to be valid, at least 75% (19 items) should have been completed.  
Anything less that that is considerd an invalid assessment.
<br><br>
For the CD-RISC-10, at least 7 of the 10 items should be completed in order to 
consider the scale as valid. For missing answers, the mean of all completed 
answers can be used to impute the score.
<br><br>

<b>10.	 Is the scale easy to read?</b>
<br /><br />
The Flesch Reading Ease score ranges from 75-77 for the 2, 10 and 25 item scales. 
This indicates that the scale can be easily understood by most 12 year olds. 
However, the CD-RISC has been successfully used in studies of children as young 
as 10 years of age (e.g. Vetter et al, 2010). In situations where the subject is 
either restricted in literacy skills or has other problems with understanding the 
CD-RISC, it is recommended that an assistant read out the scale. 
<br><br>

<b>11.	 What level of education is required in order to complete the scale?</b>
<br /><br />
The Flesch-Kincaid Grade score is 5.1 for all three forms of the CD-RISC. 
It is thus expected that scale should be understood by those with a fifth grade level of education.  
<br><br>

<b>12.   I have not seen a copy of the CD-RISC, but am interested in potentially using it. Is it possible to see and review the scale before deciding on whether I want to use it?</b>
<br /><br />
We would be pleased to provide a copy of the scale for review purposes if you are unsure whether to use it. A non-refundable fee of $10 US is payable. If, later, you decide to use the scale, this fee is deducted from the applicable user fee.
<br><br>

<b>13. Is it possible to view the manual?</b>
<br /><br />
An older example of the manual can be downloaded <a href="./CD-RISC Manual 08-19-18.pdf" style="color: blue; text-decoration: underline;">here.</a> We aim to update the manual periodically. For the most recent update, please contact mail@cd-risc.com. Although we have numerous translations of the CD-RISC scale, the manual is only available in English.
<br><br>

<b>14.   Do I have to pay a fee to use the CD-RISC?</b>
<br /><br />
A fee is charged for using the scale. The amount is determined by a number of factors, including the version of the RISC, student status, the volume (or number of administrations expected) and type of activity in which the scale will be used.
<br><br>

<b>15.   Is there a charge for translations?</b>
<br /><br />
We provide up to four languages under the user cost, but for more than four then there is a charge of $10 per each translation, separately for the 2, 10 and 25 item scales.

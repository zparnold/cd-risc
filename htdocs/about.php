                                <h1 class="page_title">Introduction</h1>
                                <h4>Our interest in resilience arose from long experience treating men and women with posttraumatic stress disorder (PTSD), as well as from our broad-range research into the disorder, which covered treatment assessment, diagnosis, biological characterization, cross-cultural study, epidemiology, risk factors and the development of measures for PTSD and other forms of anxiety.</h4>
                                <img src="images/temp/about_img.jpg" alt="Sky" class="content-image pull-left">
                                <p>Among the measures which we felt needed a stronger presence in the field of PTSD assessment was that of resilience. Although several scales addressing aspects of resilience have existed for some time, they had largely failed to penetrate into the world of clinical practice. In our treatment studies of PTSD we noticed that besides reducing the symptoms of the disorder, subjects became better able to handle stress and seemed to become more resilient.</p>
                                <p>After a search of the resilience literature, which for a long time was heavily influenced by contributions from the specialties of developmental psychology and child psychiatry, we selected 17 domains, as outlined in Table 1 of our publication in 2003, and developed a 25 item scale (Connor KM, Davidson JRT. Depression and Anxiety 2003; 18: 71-82). This report presented psychometric data establishing validity and reliability of the scale (known as the Connor-Davidson Resilience Scale or CD-RISC), as well as briefly describing each of the 25 items (see table 2 of that citation), and giving general scoring directions. However, the complete wording for each item and full directions did not appear in the report and the scale cannot be adequately reconstructed from the information given in this publication.</p>
                                <p>In the original validation study, mean scores in specific populations were reported as follows:</p>


                                <p>Besides the full 25-item CD-RISC (or CD-RISC 25), there are two briefer versions, the 10 item (CD-RISC 10) and two item (CD-RISC 2) scales. The 10 item version (score range 0-40) comprises items 1, 4, 6, 7, 8, 11, 14, 16, 17, 19 from the original scale, and was developed by Drs. Campbell-Sills and Stein, at the University of California, San Diego, on the basis of factor analysis. In a community survey of 764 US adults, a mean score of 31.8 (SD = 5.4) was obtained for the CD-RISC 10 (Campbell-Sills L, Forde DR, Stein MB. J Psychiatric Research (2009), doi:10.1016/j.jpsychires.2009.01.013). The population quartile scores for the CD-RISC-10 are as follows: 25th % = 29; 50th % = 32; 75th % = 36. An almost identical mean score was obtained by Davidson in the US general population sample studied in the 2003 report above.</p>
                                <p>The CD-RISC 2 is based on items 1 and 8 (score range from 0-8), and was developed as a measure of "bounce-back" and adaptability by the original authors (Vaishnavi et al, 2007). In a general population survey of US adults, mean CD-RISC 2 score was 6.91, while lower scores were observed in psychiatric groups with depression (5.12), GAD (4.96) and PTSD (4.70) (Vaishnavi et al, 2007) and in survivors of the Southeast Asian Tsunami of 2004 (4.67) (Irmansyah et al, 2010).</p>
                                <p>The 2-, 10- and 25-item versions are the only ones that have been adequately tested and validated and are the only ones authorized for use.</p>
                                <p>The CD-RISC literature continues to grow: the scale has now been translated into many different languages and studied in a variety of populations, including large community samples, survivors of various traumas, Alzheimer's caregivers, adolescents, elders, patients in treatment for PTSD, members of different ethnic groups and cultures, and selected professional or athletic groups (e.g. university students, nurses, social workers, physicians, military medical personnel, medical students, missionaries, cricketers). The CD-RISC has been included in functional neuroimaging studies, studies which utilized genotyping and studies which assessed treatment outcome. Psychometric properties of the RISC hold up in nearly all studies, although its factor structure and mean score varies with setting. For this reason, we do not recommend separate scoring of the factor subscales which were originally reported by Connor and Davidson, even though some interesting findings have been reported when specific factors or items were selected (e.g. Laff, 2008; Garcia-Izquierdo et al, 2009).</p>
